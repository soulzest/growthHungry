package TicTakToe;

public class version3 {
    public static void main(String[] args) {
        //create 3X3 array
        int n = 0;
        char[][] board = new char[n][n];

        //char array
        for(int i=0;i<n;i++){
            for(int j=0;j<n;j++){
                board[i][j] = '-';
            }
        }
    }
 
}
